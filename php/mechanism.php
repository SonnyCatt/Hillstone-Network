    <h1><i class="fa fa-graduation-cap" aria-hidden="true"></i>人事行政部</h1>
    <p>①组织：即制定、修改关于权限和职能责任的组织结构，建立双轨的、相互的、纵向及横向的信息交流系统。</p>
<p>②计划：即预测对于工作人员的需求，做出人员投入计划，并对所需要的管理政策和计划做出预先设想。</p>
<p>③人员的配备和使用：即按照工作需要，对工作人员进行录用、调配、考核、奖惩、安置等。</p>
<p>④培训：即帮助工作人员不断提高个人工作能力，进行任职前培训和在职培训。</p>
<p>⑤工资福利：即根据按劳分配的原则，做好工作人员的工资定级、升级和各种保险福利工作。</p>
<p>⑥政治思想工作：即通过各种教育方式，提高工作人员的思想政治觉悟，激励工作人员的积极性、创造性。</p>
<p>⑦人事管理研究：即对工作情况和程序进行总结、评价，以便改进管理工作。</p>
    <h1><i class="fa fa-graduation-cap" aria-hidden="true"></i>财务部</h1>
    <p>1.起草公司年度经营计划；组织编制公司年度财务预算；执行、监督、检查、总结经营计划和预算的执行情况，提出调整建议。</p>
<p>2.执行国家的财务会计政策、税收政策和法规；制订和执行公司会计政策、纳税政策及其管理政策。</p>
<p>3.整合公司业务体系资源，发挥公司综合优势，实现公司整体利益的最大化。</p>
<p>4.公司的会计核算、会计监督工作；公司会计档案管理及合同（协议）、有价证券、抵（质）押法律凭证的保管。</p>
<p>5.编写公司经营管理状况的财务分析报告。</p>
<p>6.负责公司股权管理工作，实施对全资子公司、控股公司、最大股东公司、参股公司的日常管理、财务监督及股利收缴工作。</p>
<p>7.组织经济责任制的实施工作，下达各中心核算与考核指标，组织业务考核和评价。</p>
<p>8.综合统计并分析公司债务和现金流量及各项业务情况。</p>
<p>9.研究公司融资风险和资本结构，进行融资成本核算，提出融资计划和方案；防范融资风险。</p>
<p>10.负责公司存货及低值易耗品盘点核对。会同公司综合部、技术部、采购部、仓库等有关部门做好盘点清查工作，并提出日常采购、领用和保管等工作建议和要求。</p>
<p>11.公司总经理授权或交办的其它工作。</p>
    <h1><i class="fa fa-graduation-cap" aria-hidden="true"></i>销售部</h1>
    <p>1、根据公司及部门销售任务开展销售工作，完成各项销售指标；</p>
<p>2、对现有市场和客户进行分析，发现客户的潜在需求，引导相关客户的现有需求，达成销售目标；</p>
<p>3、根据客户需求的特点，为客户提供最优产品解决方案；</p>
<p>4、负责与客户之间的商务谈判，建立客户关系，提高合同额和利润率；</p>
<p>5、负责协调各种内部、外部资源，解决在项目实施过程中出现的问题，提高客户满意度；</p>
<p>6、负责对项目回款过程的监控和执行，提高回款率；</p>
<p>7、通过客户回访，了解客户需求及潜在需求，以提高客户满意，促成客户二次开发；</p>
<p>8、上级领导交待的其他工作事项。</p>
    <h1><i class="fa fa-graduation-cap" aria-hidden="true"></i>技术部</h1>
    <p>a.负责对新产品的设计和开发的控制及编制各类技术文件；</p>
 <p>b.参与产品实现的策划；</p>
<p>c.参与合同评审和对供方的评价。</p>
<p>d.负责对质量管理体系的日常管理工作，包括内部审核工作；</p>
<p>e.负责对监视和测量装置的管理工作；</p>
<p>f.负责产品的监视和测量控制工作；</p>
<p>g.负责对质量管理体系过程的监视和测量；</p>
<p>h.是不合格品控制的归口管理部门；</p>
<p>i.负责数据分析管理工作及纠正、预防措施的评审工作。</p>
